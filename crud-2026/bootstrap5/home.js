function generarTabla(json) {
    if (!json.length) return;

    if ($.fn.DataTable.isDataTable("#tblDynamic")) {
        $("#tblDynamic").DataTable().destroy();
    }

    const headers = Object.keys(json[0]);
    let html = `<table id="tblDynamic" class="table table-bordered table-striped w-100">`;

    html += `<thead><tr>`;

    html += `<th>Editar</th><th>Anular</th>`;
    headers.forEach(h => {
        if (isIgnoredColumn(h)) return;
        if (!isHiddenColumn(h)) html += `<th>${h}</th>`;
    });
    
    html += `</tr></thead>`;

    html += `<tbody>`;
    json.forEach(row => {
        html += `<tr>`;

        html += `
        <td><button class="btn btn-primary btn-sm" onclick="abrirTab(${row.id})">Editar</button></td>
        <td><button class="btn btn-danger btn-sm" onclick="anular(${row.id})">Anular</button></td>
        `;

        headers.forEach(h => {
            if (isIgnoredColumn(h)) return;
            if (isHiddenColumn(h)) return;

            let value = row[h];

            if (isDateColumn(h)) value = formatDate(value);

            if (h === "Estado") {
                const cls = estadoColors[value] || "bg-secondary text-white";
                value = `<span class="badge ${cls} chip-glow">${value}</span>`;
            }

        html += `<td>${value}</td>`;
        });

        

        html += `</tr>`;
  });
  html += `</tbody></table>`;

  document.getElementById("tablaContainer").innerHTML = html;

  $("#tblDynamic").DataTable({
    "order"     : [[ 2, "desc" ]],
    "scrollX"   : true,
    lengthMenu : [
        [ 50 , 100, 200 ],
        [ 50 , 100, 200 ],
    ],
    responsive: true,
    //pageLength: 5,
    dom: "Bfrtip",
    buttons: ["excelHtml5", "pdfHtml5", "csvHtml5", "print"],
    //language: { url: "https://cdn.datatables.net/plug-ins/1.13.6/i18n/es-ES.json" } , 
    language : optsLangDatatable,
    
  });
}
