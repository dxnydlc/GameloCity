  const tabHtml = `
    <li class="nav-item" id="li-${tabId}" style="border-left: 1px silver solid;border-bottom: 1px silver solid;" >
      <button class="nav-link d-flex align-items-center gap-2"
              id="${tabId}" data-bs-toggle="tab"
              data-bs-target="#${tabContentId}">
        ${id} ${chip}
        <span class="badge bg-dangerxxx " onclick="cerrarTab(event,'${tabId}','${tabContentId}')">✖</span>
      </button>
    </li>`;

  document.getElementById("editorTabs").insertAdjacentHTML("beforeend", tabHtml);

