function handleFail(xhr, status, error) {
    const httpStatus = xhr.status;

    const handlers = {
        0:   () => tostada('Error', 'No hay conexión con el servidor', 'error'),
        400: () => tostada('Solicitud inválida', xhr.responseJSON?.message || 'Revise los datos enviados', 'warning'),
        401: () => tostada('No autorizado', 'Su sesión ha expirado o no tiene permisos', 'error'),
        403: () => tostada('Prohibido', 'No tiene permisos para realizar esta acción', 'error'),
        404: () => tostada('No encontrado', 'El recurso solicitado no existe', 'warning'),
        409: () => tostada('Conflicto', xhr.responseJSON?.message || 'El registro ya existe o está en uso', 'warning'),
        422: () => mostrarErroresValidacion(xhr),
        500: () => tostada('Error interno', 'Ocurrió un problema en el servidor', 'error'),
        503: () => tostada('Servicio no disponible', 'El servidor está en mantenimiento', 'error')
    };

    // Ejecuta el handler si existe, si no, usa uno genérico
    (handlers[httpStatus] || (() => {
        tostada('Error', `Error inesperado (${httpStatus})`, 'error');
    }))();
}
