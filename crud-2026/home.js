/* ------------------------------------------------------------- */
function prepararRequest( tipoReq ) {
    let data            = {};
    let id              = 0;
    let uu_id           = ``;

    let xUrl            = ``;
    let xMetodo         = ``;
    let esEdicion     = id > 0;

    // listar-cab | guardar-cab | cargar-cab | anular-cab
    switch ( tipoReq ) {
        // -------------------------------------------------------------
        case 'guardar-cab':
            data            = $('#frmDocumento').serialize();
            id              = parseInt( $('#frmDocumento #id').val() );
            uu_id           = $('#frmDocumento #uu_id').val();

            idCab           = id;
            uuidCab         = uu_id;
            xUrl            = `${urlServicio}guardar`;
            xMetodo         = `POST`;

            if( id > 0 ){
                xUrl            = `${urlServicio}actualizar/${uu_id}`;
                xMetodo         = `PATCH`;
            }
        break;
        // -------------------------------------------------------------
        case 'listar-cab':
            xUrl             = `${urlServicio}`, 
            xMetodo          = `GET`;
        break;
        // -------------------------------------------------------------
        case 'cargar-cab':
            xUrl             = `${urlServicio}get-by-id/${idCab}`, 
            xMetodo          = `GET`;
        break;
        // -------------------------------------------------------------
        case 'anular-cab':
            xUrl             = `${urlServicio}${uuidCab}`;
            xMetodo          = `DELETE`;
        break;
        // -------------------------------------------------------------
        case 'get-locales':
            xUrl             = `${_URL_NESTMy}v1/sucursales/get-by-cliente/${xIdClienteProv}`;
            xMetodo          = `GET`;
        break;
        // -------------------------------------------------------------
        // -------------------------------------------------------------
        // -------------------------------------------------------------
        // -------------------------------------------------------------
        // -------------------------------------------------------------
        // -------------------------------------------------------------
        // -------------------------------------------------------------
        // -------------------------------------------------------------
        // -------------------------------------------------------------
        // -------------------------------------------------------------
        // -------------------------------------------------------------
        // -------------------------------------------------------------
        // -------------------------------------------------------------
        // -------------------------------------------------------------
        // -------------------------------------------------------------
        // -------------------------------------------------------------
        default:
        break;
        // -------------------------------------------------------------
        // -------------------------------------------------------------
        // -------------------------------------------------------------
    }

    

    return {
        url     : xUrl , 
        metodo  : xMetodo , 
        data 
    };
}

