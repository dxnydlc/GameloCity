function ejecutarDoc( tipoReq ) {
    try {
        // listar-cab | guardar-cab | cargar-cab | anular-cab
        // get-locales
        let objCargando = ``;
        varDump( tipoReq );

        switch (tipoReq) {
            case 'listar-cab'  : objCargando = `#TablaHomePs`; break;
            case 'guardar-cab' : objCargando = `#frmDocumento`; break;
            case 'anular-cab'  : objCargando = `#wrapper_form`; break;
            case 'cargar-cab'  : objCargando = `#frmDocumento`; break;
        }

        mostrarLoader( objCargando );

        let { url, metodo, data } = prepararRequest( tipoReq );
        console.log( url, metodo, data );

        $.ajax({
            url ,
            method  : metodo,
            data    ,
            dataType    : "json",
            headers : {
                Authorization: `Bearer ${_session3001}`
            }
        })
        .done( function(json, textStatus, xhr) { handleSuccess( json , textStatus, xhr , tipoReq ) } )
        .fail( handleFail )
        .always( () => ocultarLoader( objCargando ) );

    } catch (error) {
        alert(error);
        ocultarLoader( objCargando );
    }
}
