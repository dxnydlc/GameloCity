function generarTabla(json) {
    if (!json.length) return;

    if ($.fn.DataTable.isDataTable("#tblDynamic")) {
        $("#tblDynamic").DataTable().destroy();
    }

    const headers = Object.keys(json[0]);
    let html = `<table id="tblDynamic" class="table table-bordered table-striped">`;

    html += `<thead><tr>`;

    html += `<th>Editar</th><th>Anular</th>`;

  headers.forEach(h => {
    if (!isIgnoredColumn(h) && !isHiddenColumn(h)) {
      html += `<th>${h}</th>`;
    }
  });
  html += `</tr></thead>`;

  html += `<tbody>`;
  json.forEach(row => {
    html += `<tr>`;

    html += `
      <td><button class="btn btn-primary btn-sm" onclick="abrirTab(${row.id})"><i class="fa fa-pencil" ></i></button></td>
      <td><button class="btn btn-danger btn-sm"  onclick="anular(${row.id})"><i class="fa fa-trash" ></i></button></td>
    `;

    headers.forEach(h => {
      if (isIgnoredColumn(h) || isHiddenColumn(h)) return;

      let value = row[h];

      if (isDateColumn(h)) value = formatDate(value);

      if (h === "Estado") {
        const cls = estadoColors[value] || "label-default";
        value = `<span class="label ${cls} chip-glow">${value}</span>`;
      }

      html += `<td>${value}</td>`;
    });

    
    html += `</tr>`;
  });
  html += `</tbody></table>`;

  $("#tablaContainer").html(html);

  $("#tblDynamic").DataTable({
    paging: true,
    searching: true,
    ordering: true,
    autoWidth: false,
    language: {
      url: "https://cdn.datatables.net/plug-ins/1.13.6/i18n/es-ES.json"
    }
  });
}

