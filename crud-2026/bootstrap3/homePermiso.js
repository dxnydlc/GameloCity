  $(`#li-${oldTabId}`).attr("id", `li-${newTabId}`);

  // Renombrar <a>
  $(`#${oldTabId}`)
    .attr("id", newTabId)
    .attr("href", `#${newContentId}`)
    .html(`${newId} <span class="label label-danger" onclick="cerrarTab(event,'${newTabId}','${newContentId}')">✖</span>`);

  // Renombrar contenido
  $(`#${oldContentId}`).attr("id", newContentId);

  // Activar el tab renombrado
  $(`#${newTabId}`).tab("show");
