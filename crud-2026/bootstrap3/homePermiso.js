function isHiddenColumn(key)   { return columnasOcultas.includes(key); }
function isIgnoredColumn(key)  { return columnasIgnoradas.includes(key); }
function isReadonlyColumn(key) { return columnasReadonly.includes(key); }
function isNumericColumn(key)  { return columnasNumericas.includes(key); }
function isTextColumn(key)     { return columnasTexto.includes(key); }
function isDateColumn(key)     { return columnasFecha.includes(key); }
function isTimeColumn(key)     { return columnasHora.includes(key); }
function isSelect2Column(key)  { return columnasSelect2.includes(key); }

function getInputType(key) {
  if (isNumericColumn(key)) return "number";
  if (isDateColumn(key))    return "datetime-local";
  if (isTimeColumn(key))    return "time";
  return "text";
}

function getDefaultValue(key) {
  const val = columnasDefault[key];

  if (typeof val === "function") {
    return val(); // ejecutar función
  }

  return val ?? "";
}


function formatDate(value) {
  if (!value) return "";
  return moment(value.replace("-", "").replace(" ", "T")).format("DD/MM/YYYY HH:mm");
}



