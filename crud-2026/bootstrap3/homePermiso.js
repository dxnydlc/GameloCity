function generarDosCaracteres() {
  const chars = 'ABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789';
  let resultado = '';

  for (let i = 0; i < 2; i++) {
    const index = Math.floor(Math.random() * chars.length);
    resultado += chars[index];
  }

  return resultado;
}
